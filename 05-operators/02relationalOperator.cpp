#include<bits/stdc++.h>
using namespace std;
int main(void)
{
    int a = 6, b = 4;

    //equal operator
    cout<<"a == b is "<<(a == b)<<endl;

    // greater than operator
    cout<<"a > b is "<<(a > b)<<endl;

    // greater than equal to operator
    cout<<"a >= b is "<<(a >= b)<<endl;

    // less than operator
    cout<<"a < b is "<<(a < b)<<endl;

    // less than equal to operator
    cout<<"a <= b is "<<(a <= b)<<endl;

    // not equal to operator
    cout<<"a != b is "<<(a != b)<<endl;

    return 0;
}