#include<bits/stdc++.h>
using namespace std;
int main(void)
{
    int a = 8, b = 3;

    //addition 
    cout<<"a + b = "<<(a+b)<<endl;

    //substraction
    cout<<"a - b = "<<(a-b)<<endl;

    //multiplication
    cout<<"a * b = "<<(a*b)<<endl;

    //division
    cout<<"a / b = "<<(a/b)<<endl;

    //modulo
    cout<<"a % b = "<<(a%b)<<endl;

    //increment
    cout<<"++a = "<<(++a)<<endl;

    //decrement
    cout<<"--b = "<<(--b)<<endl;

    return 0;
}