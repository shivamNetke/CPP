#include<bits/stdc++.h>
using namespace std;

int main(void)
{
    int a = 6, b = 4;

    // logical AND operator
    cout<<"a && b is "<<(a && b)<<endl;
    
    // logical OR operator
    cout<<"a || b is "<<(a || b)<<endl;

    // logical NOT operator
    cout<<"!b is "<<(!b)<<endl;

    return 0;
}